def get_day_of_week(number_str):
    """
    Аналізує введене число та повертає відповідний день тижня.

    Args:
        number_str: Рядок, що представляє введене користувачем число.

    Returns:
        Рядок з назвою дня тижня або повідомлення про помилку.
    """
    days = {
        1: "Понеділок",
        2: "Вівторок",
        3: "Середа",
        4: "Четвер",
        5: "П'ятниця",
        6: "Субота",
        7: "Неділя"
    }
    try:
        number = int(number_str)
        if 1 <= number <= 7:
            return days[number]
        else:
            return "Введено число поза межами 1-7."
    except ValueError:
        return "Введено некоректні дані. Будь ласка, введіть число від 1 до 7."

if __name__ == "__main__":
    while True:
        user_input = input("Будь ласка, введіть число від 1 до 7: ")
        result = get_day_of_week(user_input)
        print(result)
        if result != "Введено некоректні дані. Будь ласка, введіть число від 1 до 7." and result != "Введено число поза межами 1-7.":
            break # Вийти з циклу після успішного введення